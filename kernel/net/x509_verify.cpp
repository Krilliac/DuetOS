// ---------------------------------------------------------------------------
// Parsed cert view: the structural fields from crypto::x509 plus the
// extras this layer needs (SAN list, raw SPKI key bytes).
// ---------------------------------------------------------------------------

// SAN id-ce-subjectAltName = 2.5.29.17 -> DER body 0x55 0x1D 0x11.
constexpr u8 kOidSubjectAltName[] = {0x55, 0x1D, 0x11};

struct ParsedCert
{
    duetos::crypto::x509::Certificate base{};

    // subjectAltName dNSName entries: slices into the DER.
    static constexpr u32 kMaxSan = 16;
    const u8* san_dns[kMaxSan]{};
    u32 san_dns_len[kMaxSan]{};
    u32 san_count = 0;

    // notBefore / notAfter resolved to Unix seconds. kBadTime marks a
    // value that did not parse — any cert with a bad time fails closed.
    static constexpr u64 kBadTime = ~u64(0);
    u64 not_before = kBadTime;
    u64 not_after = kBadTime;

    bool ok = false;
};

// Convert a single decimal digit byte; returns 0xFF if non-digit.
u8 DecDigit(u8 c)
{
    return (c >= '0' && c <= '9') ? static_cast<u8>(c - '0') : static_cast<u8>(0xFF);
}

// Parse `n` decimal digits starting at p[0]; on any non-digit returns
// false. Writes the accumulated value to *out.
bool ParseDecN(const u8* p, u32 n, u32* out)
{
    u32 v = 0;
    for (u32 i = 0; i < n; ++i)
    {
        const u8 d = DecDigit(p[i]);
        if (d == 0xFF)
            return false;
        v = v * 10 + d;
    }
    *out = v;
    return true;
}

// Parse an X.509 Time value into Unix seconds.
//
//   UTCTime          "YYMMDDHHMMSSZ"  (13 bytes, RFC 5280 mandates Z +
//                    seconds). YY < 50 => 20YY, else 19YY.
//   GeneralizedTime  "YYYYMMDDHHMMSSZ" (15 bytes).
//
// Anything not matching exactly (no Z, fractional seconds, offset form)
// fails closed: certs in the wild that we cannot represent are treated
// as untrustworthy rather than guessed at.
bool ParseTime(const u8* v, u32 len, u8 tag, u64* out)
{
    if (v == nullptr)
        return false;

    i32 year = 0;
    u32 idx = 0;
    if (tag == duetos::crypto::asn1::kTagUtcTime)
    {
        if (len != 13 || v[12] != 'Z')
            return false;
        u32 yy = 0;
        if (!ParseDecN(v, 2, &yy))
            return false;
        year = (yy < 50) ? static_cast<i32>(2000 + yy) : static_cast<i32>(1900 + yy);
        idx = 2;
    }
    else if (tag == duetos::crypto::asn1::kTagGeneralizedTime)
    {
        if (len != 15 || v[14] != 'Z')
            return false;
        u32 yyyy = 0;
        if (!ParseDecN(v, 4, &yyyy))
            return false;
        year = static_cast<i32>(yyyy);
        idx = 4;
    }
    else
    {
        return false;
    }

    u32 mo = 0, da = 0, hh = 0, mi = 0, ss = 0;
    if (!ParseDecN(v + idx, 2, &mo) || !ParseDecN(v + idx + 2, 2, &da) || !ParseDecN(v + idx + 4, 2, &hh) ||
        !ParseDecN(v + idx + 6, 2, &mi) || !ParseDecN(v + idx + 8, 2, &ss))
        return false;
    if (mo < 1 || mo > 12 || da < 1 || da > 31 || hh > 23 || mi > 59 || ss > 60)
        return false;

    duetos::util::DateTime dt{};
    dt.year = year;
    dt.month = static_cast<u8>(mo);
    dt.day = static_cast<u8>(da);
    dt.hour = static_cast<u8>(hh);
    dt.minute = static_cast<u8>(mi);
    dt.second = static_cast<u8>(ss);
    const u64 secs = duetos::util::UnixSecsFromDateTime(dt);
    if (secs == duetos::util::kJulianDayInvalid)
        return false;
    *out = secs;
    return true;
}

// Walk TBSCertificate to find the v3 extensions block ([3] EXPLICIT,
// tag 0xA3) and, within it, the subjectAltName extension. Extracts the
// dNSName ([2] context-primitive, tag 0x82) entries.
//
// This re-walks the TBS rather than depending on crypto::x509 surfacing
// extensions (it does not). Every step is bounds-checked against the
// enclosing element so malformed/oversized lengths fail closed.
void ParseSanFromTbs(const Element& tbs, ParsedCert* pc)
{
    using namespace duetos::crypto::asn1;
    if (tbs.tag != kTagSequence)
        return;

    // Step over the fixed TBS prefix to reach extensions:
    //   [0] version (opt) | serial | sigAlg | issuer | validity |
    //   subject | SPKI | [3] extensions (opt)
    u32 off = 0;
    Element e{};
    if (Read(tbs.value, tbs.len, &e) != Status::Ok)
        return;
    if (e.tag == 0xA0) // [0] EXPLICIT version
        off += e.header_len + e.len;

    // Skip serial, sigAlg, issuer, validity, subject, SPKI (6 elements).
    for (u32 i = 0; i < 6; ++i)
    {
        if (off >= tbs.len)
            return;
        if (Read(tbs.value + off, tbs.len - off, &e) != Status::Ok)
            return;
        off += e.header_len + e.len;
    }

    // Now scan remaining children for the [3] extensions wrapper.
    while (off < tbs.len)
    {
        if (Read(tbs.value + off, tbs.len - off, &e) != Status::Ok)
            return;
        const u32 step = e.header_len + e.len;
        if (e.tag == 0xA3) // [3] EXPLICIT extensions
        {
            // Inside: SEQUENCE OF Extension.
            Element ext_seq{};
            if (Read(e.value, e.len, &ext_seq) != Status::Ok || ext_seq.tag != kTagSequence)
                return;
            u32 xo = 0;
            while (xo < ext_seq.len)
            {
                Element ext{};
                if (Read(ext_seq.value + xo, ext_seq.len - xo, &ext) != Status::Ok || ext.tag != kTagSequence)
                    return;
                // Extension ::= SEQUENCE { extnID OID, critical BOOLEAN
                //               DEFAULT FALSE, extnValue OCTET STRING }
                Element oid{};
                if (Read(ext.value, ext.len, &oid) == Status::Ok && oid.tag == kTagOid &&
                    OidEquals(oid, kOidSubjectAltName, sizeof(kOidSubjectAltName)))
                {
                    // Find the OCTET STRING (skip optional critical BOOLEAN).
                    u32 eo = oid.header_len + oid.len;
                    while (eo < ext.len)
                    {
                        Element fld{};
                        if (Read(ext.value + eo, ext.len - eo, &fld) != Status::Ok)
                            break;
                        if (fld.tag == kTagOctetString)
                        {
                            // extnValue wraps GeneralNames ::= SEQUENCE OF GeneralName.
                            Element gn_seq{};
                            if (Read(fld.value, fld.len, &gn_seq) == Status::Ok && gn_seq.tag == kTagSequence)
                            {
                                u32 go = 0;
                                while (go < gn_seq.len && pc->san_count < ParsedCert::kMaxSan)
                                {
                                    Element gn{};
                                    if (Read(gn_seq.value + go, gn_seq.len - go, &gn) != Status::Ok)
                                        break;
                                    if (gn.tag == 0x82) // [2] dNSName (IA5String, primitive)
                                    {
                                        pc->san_dns[pc->san_count] = gn.value;
                                        pc->san_dns_len[pc->san_count] = gn.len;
                                        ++pc->san_count;
                                    }
                                    go += gn.header_len + gn.len;
                                }
                            }
                            break;
                        }
                        eo += fld.header_len + fld.len;
                    }
                }
                xo += ext.header_len + ext.len;
            }
            return;
        }
        off += step;
    }
}

// Full parse: structural fields + validity Unix conversion + SAN.
bool ParseOne(const u8* der, u32 der_len, ParsedCert* pc)
{
    *pc = ParsedCert{};
    if (der == nullptr || der_len == 0 || der_len > kMaxCertDer)
        return false;

    if (duetos::crypto::x509::Parse(der, der_len, &pc->base) != duetos::crypto::x509::Status::Ok)
        return false;

    // Resolve validity to Unix seconds. The parser surfaces the raw
    // value bytes but not the tag, so re-read the validity Time TLVs
    // from the TBS to learn UTCTime vs GeneralizedTime. crypto::x509
    // stores the value slices; recover the tag from the byte before.
    // Cheaper and safer: re-walk validity here using the asn1 reader.
    using namespace duetos::crypto::asn1;
    Element outer{};
    if (Read(der, der_len, &outer) != Status::Ok || outer.tag != kTagSequence)
        return false;
    Element tbs{};
    if (Read(outer.value, outer.len, &tbs) != Status::Ok || tbs.tag != kTagSequence)
        return false;

    // Walk to the validity SEQUENCE (4th element after optional version).
    u32 off = 0;
    Element e{};
    if (Read(tbs.value, tbs.len, &e) != Status::Ok)
        return false;
    if (e.tag == 0xA0)
        off += e.header_len + e.len;
    for (u32 i = 0; i < 3; ++i) // serial, sigAlg, issuer
    {
        if (off >= tbs.len || Read(tbs.value + off, tbs.len - off, &e) != Status::Ok)
            return false;
        off += e.header_len + e.len;
    }
    Element validity{};
    if (off >= tbs.len || Read(tbs.value + off, tbs.len - off, &validity) != Status::Ok || validity.tag != kTagSequence)
        return false;
    Element nb{};
    if (Read(validity.value, validity.len, &nb) != Status::Ok)
        return false;
    const u32 nb_step = nb.header_len + nb.len;
    Element na{};
    if (nb_step >= validity.len || Read(validity.value + nb_step, validity.len - nb_step, &na) != Status::Ok)
        return false;
    if (!ParseTime(nb.value, nb.len, nb.tag, &pc->not_before))
        return false;
    if (!ParseTime(na.value, na.len, na.tag, &pc->not_after))
        return false;
    if (pc->not_before > pc->not_after)
        return false;

    ParseSanFromTbs(tbs, pc);
    pc->ok = true;
    return true;
}

// SHA-256(tbs) and PKCS#1 v1.5 verify against the issuer's RSA key.
// sha256WithRSAEncryption only; any other sig algorithm fails closed.
bool VerifySig(const ParsedCert& cert, const duetos::crypto::RsaPublicKey& issuer_key)
{
    if (cert.base.sig_algo != duetos::crypto::x509::SignatureAlgo::Sha256WithRsa)
        return false; // GAP: ECDSA / Ed25519 / RSA-PSS unsupported — fail closed.
    if (cert.base.tbs == nullptr || cert.base.tbs_len == 0 || cert.base.signature == nullptr)
        return false;

    u8 digest[duetos::crypto::kSha256DigestBytes];
    duetos::crypto::Sha256Hash(cert.base.tbs, cert.base.tbs_len, digest);

    return duetos::crypto::RsaPkcs1V15Verify(issuer_key, cert.base.signature, cert.base.signature_len,
                                             duetos::crypto::kPkcs1Sha256DigestPrefix,
                                             duetos::crypto::kPkcs1Sha256DigestPrefixLen, digest, sizeof(digest));
}

// Case-insensitive ASCII compare of a DNS label slice to a hostname
// span [hp, hp+hlen). Lengths must match.
bool DnsEqual(const u8* a, u32 alen, const char* hp, u32 hlen)
{
    if (alen != hlen)
        return false;
    for (u32 i = 0; i < alen; ++i)
    {
        u8 x = a[i];
        u8 y = static_cast<u8>(hp[i]);
        if (x >= 'A' && x <= 'Z')
            x = static_cast<u8>(x + 32);
        if (y >= 'A' && y <= 'Z')
            y = static_cast<u8>(y + 32);
        if (x != y)
            return false;
    }
    return true;
}

// Match one SAN dNSName pattern against the hostname. Supports a single
// leftmost "*." wildcard that matches exactly one label (RFC 6125):
// "*.example.com" matches "foo.example.com" but NOT "example.com" nor
// "a.b.example.com". A bare "*" or wildcard not in the leftmost label
// is rejected.
bool DnsNameMatches(const u8* pat, u32 patlen, const char* host, u32 hostlen)
{
    if (patlen == 0 || hostlen == 0)
        return false;

    // Wildcard form: must be exactly "*." prefix.
    if (pat[0] == '*')
    {
        if (patlen < 2 || pat[1] != '.')
            return false; // "*" alone or "*foo" — reject.
        // The wildcard covers the host's leftmost label. Find the first
        // dot in the host; the remainder (incl. the dot) must equal the
        // pattern remainder (incl. its dot).
        u32 hd = 0;
        while (hd < hostlen && host[hd] != '.')
            ++hd;
        if (hd == hostlen)
            return false; // host has no dot -> no label to consume.
        if (hd == 0)
            return false;                       // empty leftmost label.
        const char* host_rest = host + hd;      // points at '.'
        const u32 host_rest_len = hostlen - hd; // includes the dot
        const u8* pat_rest = pat + 1;           // points at '.'
        const u32 pat_rest_len = patlen - 1;    // includes the dot
        // Reject if the wildcard's tail itself contains another label
        // wildcard or the host remainder still has a wildcard-y char —
        // both spans are literal here, so just compare.
        return DnsEqual(pat_rest, pat_rest_len, host_rest, host_rest_len);
    }

    return DnsEqual(pat, patlen, host, hostlen);
}

bool HostMatches(const ParsedCert& leaf, const char* hostname)
{
    if (hostname == nullptr || hostname[0] == '\0')
        return false;
    u32 hlen = 0;
    while (hostname[hlen] != '\0')
        ++hlen;

    // Prefer SAN. RFC 6125 / 2818: if a SAN dNSName list is present, the
    // CN is NOT consulted. Only fall back to CN when no SAN dNSName.
    if (leaf.san_count > 0)
    {
        for (u32 i = 0; i < leaf.san_count; ++i)
            if (DnsNameMatches(leaf.san_dns[i], leaf.san_dns_len[i], hostname, hlen))
                return true;
        return false;
    }

    // CN fallback (with wildcard support).
    if (leaf.base.subject_cn != nullptr && leaf.base.subject_cn_len > 0)
        return DnsNameMatches(leaf.base.subject_cn, leaf.base.subject_cn_len, hostname, hlen);
    return false;
}

bool WithinValidity(const ParsedCert& c, u64 now_unix)
{
    if (c.not_before == ParsedCert::kBadTime || c.not_after == ParsedCert::kBadTime)
        return false;
    return now_unix >= c.not_before && now_unix <= c.not_after;
}

