/*
 * Verification flow (see header for the security contract):
 *
 *   ParseOne()      structural parse via crypto::x509::Parse, plus a
 *                   second TBS walk to pull the SAN dNSName list and
 *                   the SubjectPublicKeyInfo BIT STRING bytes (needed
 *                   so the embedded trust store can identify an anchor
 *                   by exact key bytes).
 *   VerifySig()     SHA-256(tbs) -> crypto::RsaPkcs1V15Verify against a
 *                   candidate issuer key. sha256WithRSAEncryption only.
 *   ParseTime()     UTCTime / GeneralizedTime value -> Unix seconds.
 *   HostMatches()   SAN dNSName (leftmost "*." wildcard) else CN.
 *   Verify()        builds leaf -> [intermediate] -> trusted-root and
 *                   AND-folds every check; fails closed on anything.
 */

