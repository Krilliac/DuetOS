// ---------------------------------------------------------------------------
// Parsed cert view: the structural fields from crypto::x509 plus the
// extras this layer needs (SAN list, raw SPKI key bytes).
// ---------------------------------------------------------------------------

// SAN id-ce-subjectAltName = 2.5.29.17 -> DER body 0x55 0x1D 0x11.
constexpr u8 kOidSubjectAltName[] = {0x55, 0x1D, 0x11};

// ECDSA OIDs (DER body bytes). The structural parser (crypto::x509)
// only models RSA SPKI + RSA signature algorithms, so this layer
// re-walks the cert to recognise the EC shapes itself.
//   id-ecPublicKey            1.2.840.10045.2.1
//   prime256v1 (P-256)        1.2.840.10045.3.1.7
//   secp384r1  (P-384)        1.3.132.0.34
//   ecdsa-with-SHA256         1.2.840.10045.4.3.2
//   ecdsa-with-SHA384         1.2.840.10045.4.3.3
constexpr u8 kOidEcPublicKey[] = {0x2A, 0x86, 0x48, 0xCE, 0x3D, 0x02, 0x01};
constexpr u8 kOidCurveP256[] = {0x2A, 0x86, 0x48, 0xCE, 0x3D, 0x03, 0x01, 0x07};
constexpr u8 kOidCurveP384[] = {0x2B, 0x81, 0x04, 0x00, 0x22};
constexpr u8 kOidEcdsaSha256[] = {0x2A, 0x86, 0x48, 0xCE, 0x3D, 0x04, 0x03, 0x02};
constexpr u8 kOidEcdsaSha384[] = {0x2A, 0x86, 0x48, 0xCE, 0x3D, 0x04, 0x03, 0x03};

// The signature algorithm of a cert, as this layer sees it. The base
// parser surfaces RSA; we additionally classify the two ECDSA shapes.
enum class CertSigAlgo : u8
{
    Other = 0, // not a shape we verify -> fail closed
    Sha256WithRsa,
    EcdsaWithSha256,
    EcdsaWithSha384,
};

struct ParsedCert
{
    duetos::crypto::x509::Certificate base{};

    // Classified outer signature algorithm (RSA via base, ECDSA via the
    // re-walk in ParseSigAlgo).
    CertSigAlgo sig_algo = CertSigAlgo::Other;

    // EC subject public key, populated when the SPKI algorithm is
    // id-ecPublicKey with a namedCurve we support. `ec_present` gates it.
    bool ec_present = false;
    duetos::net::ec::CurveId ec_curve = duetos::net::ec::CurveId::P256;
    duetos::net::ec::Point ec_point{};

    // Issuer / subject DN TLV slices (the full Name SEQUENCE, header
    // included) into the DER. Used to gate the expensive signature
    // verify on RFC 5280 name-chaining: a candidate issuer can only
    // have signed this cert if issuer(cert) == subject(issuer) byte for
    // byte. This both enforces correct chain building AND keeps the boot
    // self-test affordable under TCG, where a needless ECDSA P-384
    // verify against a non-matching root costs tens of seconds.
    const u8* issuer_dn = nullptr;
    u32 issuer_dn_len = 0;
    const u8* subject_dn = nullptr;
    u32 subject_dn_len = 0;

    // subjectAltName dNSName entries: slices into the DER.
    static constexpr u32 kMaxSan = 16;
    const u8* san_dns[kMaxSan]{};
    u32 san_dns_len[kMaxSan]{};
    u32 san_count = 0;

    // notBefore / notAfter resolved to Unix seconds. kBadTime marks a
    // value that did not parse — any cert with a bad time fails closed.
    static constexpr u64 kBadTime = ~u64(0);
    u64 not_before = kBadTime;
    u64 not_after = kBadTime;

    bool ok = false;
};

// Convert a single decimal digit byte; returns 0xFF if non-digit.
u8 DecDigit(u8 c)
{
    return (c >= '0' && c <= '9') ? static_cast<u8>(c - '0') : static_cast<u8>(0xFF);
}

// Parse `n` decimal digits starting at p[0]; on any non-digit returns
// false. Writes the accumulated value to *out.
bool ParseDecN(const u8* p, u32 n, u32* out)
{
    u32 v = 0;
    for (u32 i = 0; i < n; ++i)
    {
        const u8 d = DecDigit(p[i]);
        if (d == 0xFF)
            return false;
        v = v * 10 + d;
    }
    *out = v;
    return true;
}

// Parse an X.509 Time value into Unix seconds.
//
//   UTCTime          "YYMMDDHHMMSSZ"  (13 bytes, RFC 5280 mandates Z +
//                    seconds). YY < 50 => 20YY, else 19YY.
//   GeneralizedTime  "YYYYMMDDHHMMSSZ" (15 bytes).
//
// Anything not matching exactly (no Z, fractional seconds, offset form)
// fails closed: certs in the wild that we cannot represent are treated
// as untrustworthy rather than guessed at.
bool ParseTime(const u8* v, u32 len, u8 tag, u64* out)
{
    if (v == nullptr)
        return false;

    i32 year = 0;
    u32 idx = 0;
    if (tag == duetos::crypto::asn1::kTagUtcTime)
    {
        if (len != 13 || v[12] != 'Z')
            return false;
        u32 yy = 0;
        if (!ParseDecN(v, 2, &yy))
            return false;
        year = (yy < 50) ? static_cast<i32>(2000 + yy) : static_cast<i32>(1900 + yy);
        idx = 2;
    }
    else if (tag == duetos::crypto::asn1::kTagGeneralizedTime)
    {
        if (len != 15 || v[14] != 'Z')
            return false;
        u32 yyyy = 0;
        if (!ParseDecN(v, 4, &yyyy))
            return false;
        year = static_cast<i32>(yyyy);
        idx = 4;
    }
    else
    {
        return false;
    }

    u32 mo = 0, da = 0, hh = 0, mi = 0, ss = 0;
    if (!ParseDecN(v + idx, 2, &mo) || !ParseDecN(v + idx + 2, 2, &da) || !ParseDecN(v + idx + 4, 2, &hh) ||
        !ParseDecN(v + idx + 6, 2, &mi) || !ParseDecN(v + idx + 8, 2, &ss))
        return false;
    if (mo < 1 || mo > 12 || da < 1 || da > 31 || hh > 23 || mi > 59 || ss > 60)
        return false;

    duetos::util::DateTime dt{};
    dt.year = year;
    dt.month = static_cast<u8>(mo);
    dt.day = static_cast<u8>(da);
    dt.hour = static_cast<u8>(hh);
    dt.minute = static_cast<u8>(mi);
    dt.second = static_cast<u8>(ss);
    const u64 secs = duetos::util::UnixSecsFromDateTime(dt);
    if (secs == duetos::util::kJulianDayInvalid)
        return false;
    *out = secs;
    return true;
}

// Walk TBSCertificate to find the v3 extensions block ([3] EXPLICIT,
// tag 0xA3) and, within it, the subjectAltName extension. Extracts the
// dNSName ([2] context-primitive, tag 0x82) entries.
//
// This re-walks the TBS rather than depending on crypto::x509 surfacing
// extensions (it does not). Every step is bounds-checked against the
// enclosing element so malformed/oversized lengths fail closed.
void ParseSanFromTbs(const Element& tbs, ParsedCert* pc)
{
    using namespace duetos::crypto::asn1;
    if (tbs.tag != kTagSequence)
        return;

    // Step over the fixed TBS prefix to reach extensions:
    //   [0] version (opt) | serial | sigAlg | issuer | validity |
    //   subject | SPKI | [3] extensions (opt)
    u32 off = 0;
    Element e{};
    if (Read(tbs.value, tbs.len, &e) != Status::Ok)
        return;
    if (e.tag == 0xA0) // [0] EXPLICIT version
        off += e.header_len + e.len;

    // Skip serial, sigAlg, issuer, validity, subject, SPKI (6 elements).
    for (u32 i = 0; i < 6; ++i)
    {
        if (off >= tbs.len)
            return;
        if (Read(tbs.value + off, tbs.len - off, &e) != Status::Ok)
            return;
        off += e.header_len + e.len;
    }

    // Now scan remaining children for the [3] extensions wrapper.
    while (off < tbs.len)
    {
        if (Read(tbs.value + off, tbs.len - off, &e) != Status::Ok)
            return;
        const u32 step = e.header_len + e.len;
        if (e.tag == 0xA3) // [3] EXPLICIT extensions
        {
            // Inside: SEQUENCE OF Extension.
            Element ext_seq{};
            if (Read(e.value, e.len, &ext_seq) != Status::Ok || ext_seq.tag != kTagSequence)
                return;
            u32 xo = 0;
            while (xo < ext_seq.len)
            {
                Element ext{};
                if (Read(ext_seq.value + xo, ext_seq.len - xo, &ext) != Status::Ok || ext.tag != kTagSequence)
                    return;
                // Extension ::= SEQUENCE { extnID OID, critical BOOLEAN
                //               DEFAULT FALSE, extnValue OCTET STRING }
                Element oid{};
                if (Read(ext.value, ext.len, &oid) == Status::Ok && oid.tag == kTagOid &&
                    OidEquals(oid, kOidSubjectAltName, sizeof(kOidSubjectAltName)))
                {
                    // Find the OCTET STRING (skip optional critical BOOLEAN).
                    u32 eo = oid.header_len + oid.len;
                    while (eo < ext.len)
                    {
                        Element fld{};
                        if (Read(ext.value + eo, ext.len - eo, &fld) != Status::Ok)
                            break;
                        if (fld.tag == kTagOctetString)
                        {
                            // extnValue wraps GeneralNames ::= SEQUENCE OF GeneralName.
                            Element gn_seq{};
                            if (Read(fld.value, fld.len, &gn_seq) == Status::Ok && gn_seq.tag == kTagSequence)
                            {
                                u32 go = 0;
                                while (go < gn_seq.len && pc->san_count < ParsedCert::kMaxSan)
                                {
                                    Element gn{};
                                    if (Read(gn_seq.value + go, gn_seq.len - go, &gn) != Status::Ok)
                                        break;
                                    if (gn.tag == 0x82) // [2] dNSName (IA5String, primitive)
                                    {
                                        pc->san_dns[pc->san_count] = gn.value;
                                        pc->san_dns_len[pc->san_count] = gn.len;
                                        ++pc->san_count;
                                    }
                                    go += gn.header_len + gn.len;
                                }
                            }
                            break;
                        }
                        eo += fld.header_len + fld.len;
                    }
                }
                xo += ext.header_len + ext.len;
            }
            return;
        }
        off += step;
    }
}

// Classify the outer signatureAlgorithm OID. The base parser already
// maps sha256WithRSA; we add the two ECDSA shapes by reading the OID out
// of the outer SEQUENCE's second element (signatureAlgorithm ::=
// AlgorithmIdentifier).
CertSigAlgo ClassifySigAlgo(const ParsedCert& pc, const u8* der, u32 der_len)
{
    using namespace duetos::crypto::asn1;
    if (pc.base.sig_algo == duetos::crypto::x509::SignatureAlgo::Sha256WithRsa)
        return CertSigAlgo::Sha256WithRsa;

    // Re-walk: outer SEQUENCE -> { tbs, signatureAlgorithm, signature }.
    Element outer{};
    if (Read(der, der_len, &outer) != Status::Ok || outer.tag != kTagSequence)
        return CertSigAlgo::Other;
    Element tbs{};
    if (Read(outer.value, outer.len, &tbs) != Status::Ok)
        return CertSigAlgo::Other;
    const u32 algo_off = tbs.header_len + tbs.len;
    if (algo_off >= outer.len)
        return CertSigAlgo::Other;
    Element algo{};
    if (Read(outer.value + algo_off, outer.len - algo_off, &algo) != Status::Ok || algo.tag != kTagSequence)
        return CertSigAlgo::Other;
    Element oid{};
    if (Read(algo.value, algo.len, &oid) != Status::Ok || oid.tag != kTagOid)
        return CertSigAlgo::Other;
    if (OidEquals(oid, kOidEcdsaSha256, sizeof(kOidEcdsaSha256)))
        return CertSigAlgo::EcdsaWithSha256;
    if (OidEquals(oid, kOidEcdsaSha384, sizeof(kOidEcdsaSha384)))
        return CertSigAlgo::EcdsaWithSha384;
    return CertSigAlgo::Other;
}

// Re-walk the TBS to its SubjectPublicKeyInfo and, if the algorithm is
// id-ecPublicKey with a namedCurve we support, parse the uncompressed EC
// point into pc->ec_point. Sets pc->ec_present on success. Leaves
// ec_present false (NOT an error) for RSA / unsupported-curve SPKIs —
// those go through the RSA path or fail closed downstream.
void ParseEcSpki(const Element& tbs, ParsedCert* pc)
{
    using namespace duetos::crypto::asn1;
    if (tbs.tag != kTagSequence)
        return;

    // Walk to the SPKI: skip [0] version (opt), serial, sigAlg, issuer,
    // validity, subject — SPKI is the 7th element (6th after version).
    u32 off = 0;
    Element e{};
    if (Read(tbs.value, tbs.len, &e) != Status::Ok)
        return;
    if (e.tag == 0xA0)
        off += e.header_len + e.len;
    for (u32 i = 0; i < 6; ++i) // serial, sigAlg, issuer, validity, subject, (then SPKI)
    {
        if (off >= tbs.len || Read(tbs.value + off, tbs.len - off, &e) != Status::Ok)
            return;
        if (i == 5)
            break; // e now holds the SPKI element
        off += e.header_len + e.len;
    }
    const Element& spki = e;
    if (spki.tag != kTagSequence)
        return;

    // SPKI ::= SEQUENCE { algorithm AlgorithmIdentifier, subjectPublicKey BIT STRING }.
    Element algo{};
    if (Read(spki.value, spki.len, &algo) != Status::Ok || algo.tag != kTagSequence)
        return;
    Element algo_oid{};
    if (Read(algo.value, algo.len, &algo_oid) != Status::Ok || algo_oid.tag != kTagOid)
        return;
    if (!OidEquals(algo_oid, kOidEcPublicKey, sizeof(kOidEcPublicKey)))
        return; // not EC — RSA handled elsewhere.

    // The namedCurve OID is the second field of the EC AlgorithmIdentifier.
    const u32 curve_off = algo_oid.header_len + algo_oid.len;
    if (curve_off >= algo.len)
        return;
    Element curve_oid{};
    if (Read(algo.value + curve_off, algo.len - curve_off, &curve_oid) != Status::Ok || curve_oid.tag != kTagOid)
        return;
    duetos::net::ec::CurveId curve_id;
    if (OidEquals(curve_oid, kOidCurveP256, sizeof(kOidCurveP256)))
        curve_id = duetos::net::ec::CurveId::P256;
    else if (OidEquals(curve_oid, kOidCurveP384, sizeof(kOidCurveP384)))
        curve_id = duetos::net::ec::CurveId::P384;
    else
        return; // GAP: P-521 / other curves unsupported — fail closed.

    // subjectPublicKey BIT STRING follows the algorithm.
    const u32 bs_off = algo.header_len + algo.len;
    if (bs_off >= spki.len)
        return;
    Element bitstr{};
    if (Read(spki.value + bs_off, spki.len - bs_off, &bitstr) != Status::Ok || bitstr.tag != kTagBitString ||
        bitstr.len < 2)
        return;
    if (bitstr.value[0] != 0x00) // no unused bits in an EC point BIT STRING
        return;

    // The remaining bytes are the SEC1 point (0x04 || X || Y).
    duetos::net::ec::Curve curve{};
    if (!duetos::net::ec::GetCurve(curve_id, &curve))
        return;
    duetos::net::ec::Point pt{};
    if (!duetos::net::ec::ParsePublicKey(curve, bitstr.value + 1, bitstr.len - 1, &pt))
        return; // bad length, not 0x04, off-curve -> fail closed.

    pc->ec_present = true;
    pc->ec_curve = curve_id;
    pc->ec_point = pt;
}

// Full parse: structural fields + validity Unix conversion + SAN.
bool ParseOne(const u8* der, u32 der_len, ParsedCert* pc)
{
    *pc = ParsedCert{};
    if (der == nullptr || der_len == 0 || der_len > kMaxCertDer)
        return false;

    if (duetos::crypto::x509::Parse(der, der_len, &pc->base) != duetos::crypto::x509::Status::Ok)
        return false;

    // Resolve validity to Unix seconds. The parser surfaces the raw
    // value bytes but not the tag, so re-read the validity Time TLVs
    // from the TBS to learn UTCTime vs GeneralizedTime. crypto::x509
    // stores the value slices; recover the tag from the byte before.
    // Cheaper and safer: re-walk validity here using the asn1 reader.
    using namespace duetos::crypto::asn1;
    Element outer{};
    if (Read(der, der_len, &outer) != Status::Ok || outer.tag != kTagSequence)
        return false;
    Element tbs{};
    if (Read(outer.value, outer.len, &tbs) != Status::Ok || tbs.tag != kTagSequence)
        return false;

    // Walk to the validity SEQUENCE (4th element after optional version).
    u32 off = 0;
    Element e{};
    if (Read(tbs.value, tbs.len, &e) != Status::Ok)
        return false;
    if (e.tag == 0xA0)
        off += e.header_len + e.len;
    for (u32 i = 0; i < 3; ++i) // serial, sigAlg, issuer
    {
        if (off >= tbs.len || Read(tbs.value + off, tbs.len - off, &e) != Status::Ok)
            return false;
        if (i == 2) // issuer Name: capture the full TLV slice
        {
            pc->issuer_dn = tbs.value + off;
            pc->issuer_dn_len = e.header_len + e.len;
        }
        off += e.header_len + e.len;
    }
    Element validity{};
    if (off >= tbs.len || Read(tbs.value + off, tbs.len - off, &validity) != Status::Ok || validity.tag != kTagSequence)
        return false;
    Element nb{};
    if (Read(validity.value, validity.len, &nb) != Status::Ok)
        return false;
    const u32 nb_step = nb.header_len + nb.len;
    Element na{};
    if (nb_step >= validity.len || Read(validity.value + nb_step, validity.len - nb_step, &na) != Status::Ok)
        return false;
    if (!ParseTime(nb.value, nb.len, nb.tag, &pc->not_before))
        return false;
    if (!ParseTime(na.value, na.len, na.tag, &pc->not_after))
        return false;
    if (pc->not_before > pc->not_after)
        return false;

    // subject Name: the element immediately after the validity SEQUENCE.
    const u32 subj_off = off + (validity.header_len + validity.len);
    Element subj{};
    if (subj_off < tbs.len && Read(tbs.value + subj_off, tbs.len - subj_off, &subj) == Status::Ok)
    {
        pc->subject_dn = tbs.value + subj_off;
        pc->subject_dn_len = subj.header_len + subj.len;
    }

    ParseSanFromTbs(tbs, pc);
    // EC public key (if this is an ECDSA cert) and outer signature-algo
    // classification (RSA or one of the two ECDSA shapes).
    ParseEcSpki(tbs, pc);
    pc->sig_algo = ClassifySigAlgo(*pc, der, der_len);
    pc->ok = true;
    return true;
}

// SHA-256(tbs) and PKCS#1 v1.5 verify against the issuer's RSA key. This
// is the RSA leg only — ECDSA certs route through VerifySigEc via
// IssuerSigns. sha256WithRSAEncryption only; sha1WithRSA / RSA-PSS fail
// closed here.
bool VerifySig(const ParsedCert& cert, const duetos::crypto::RsaPublicKey& issuer_key)
{
    if (cert.base.sig_algo != duetos::crypto::x509::SignatureAlgo::Sha256WithRsa)
        return false; // GAP: sha1WithRSA / RSA-PSS unsupported — fail closed.
    if (cert.base.tbs == nullptr || cert.base.tbs_len == 0 || cert.base.signature == nullptr)
        return false;

    u8 digest[duetos::crypto::kSha256DigestBytes];
    duetos::crypto::Sha256Hash(cert.base.tbs, cert.base.tbs_len, digest);

    return duetos::crypto::RsaPkcs1V15Verify(issuer_key, cert.base.signature, cert.base.signature_len,
                                             duetos::crypto::kPkcs1Sha256DigestPrefix,
                                             duetos::crypto::kPkcs1Sha256DigestPrefixLen, digest, sizeof(digest));
}

// Pull the leading-zero-stripped INTEGER value out of one ASN.1 INTEGER
// element, into [*out, *out + *out_len). Returns false on a malformed or
// non-positive INTEGER. The slice borrows into the DER.
bool EcdsaIntField(const Element& integer, const u8** out, u32* out_len)
{
    if (integer.tag != duetos::crypto::asn1::kTagInteger || integer.len == 0)
        return false;
    const u8* v = integer.value;
    u32 n = integer.len;
    // DER may prefix one 0x00 to keep the high bit from marking it
    // negative; strip it. A leading 0xFF (negative) is rejected — r,s are
    // always positive.
    if (v[0] & 0x80)
        return false;
    while (n > 1 && v[0] == 0x00)
    {
        ++v;
        --n;
    }
    *out = v;
    *out_len = n;
    return true;
}

// ECDSA verify: hash the TBS with the algorithm-matched SHA, parse the
// ECDSA-Sig-Value { r, s } out of the cert's signature, and verify
// against the issuer's EC public key. Fails closed on any parse error,
// algorithm mismatch, or bad signature.
bool VerifySigEc(const ParsedCert& cert, duetos::net::ec::CurveId issuer_curve,
                 const duetos::net::ec::Point& issuer_point)
{
    if (cert.base.tbs == nullptr || cert.base.tbs_len == 0 || cert.base.signature == nullptr ||
        cert.base.signature_len == 0)
        return false;

    // Hash the TBS with the SHA the signature algorithm names. The hash
    // function and the curve are independent in principle, but the web
    // PKI pairs P-256 with SHA-256 and P-384 with SHA-384; we honour the
    // signature OID, not the curve, for the digest choice.
    u8 digest[duetos::crypto::kSha384DigestBytes];
    u32 digest_len = 0;
    if (cert.sig_algo == CertSigAlgo::EcdsaWithSha256)
    {
        duetos::crypto::Sha256Hash(cert.base.tbs, cert.base.tbs_len, digest);
        digest_len = duetos::crypto::kSha256DigestBytes;
    }
    else if (cert.sig_algo == CertSigAlgo::EcdsaWithSha384)
    {
        duetos::crypto::Sha384Hash(cert.base.tbs, cert.base.tbs_len, digest);
        digest_len = duetos::crypto::kSha384DigestBytes;
    }
    else
    {
        return false; // GAP: ecdsa-with-SHA1 / SHA-512 / Ed25519 — fail closed.
    }

    // Parse the ECDSA-Sig-Value: SEQUENCE { INTEGER r, INTEGER s }. The
    // cert's `signature` slice is the BIT STRING contents (the parser
    // already stripped the unused-bits byte), which is exactly this DER
    // SEQUENCE.
    using namespace duetos::crypto::asn1;
    Element sig_seq{};
    if (Read(cert.base.signature, cert.base.signature_len, &sig_seq) != Status::Ok || sig_seq.tag != kTagSequence)
        return false;
    Element r_int{};
    if (Read(sig_seq.value, sig_seq.len, &r_int) != Status::Ok)
        return false;
    const u32 r_step = r_int.header_len + r_int.len;
    if (r_step >= sig_seq.len)
        return false;
    Element s_int{};
    if (Read(sig_seq.value + r_step, sig_seq.len - r_step, &s_int) != Status::Ok)
        return false;

    const u8 *r_bytes = nullptr, *s_bytes = nullptr;
    u32 r_len = 0, s_len = 0;
    if (!EcdsaIntField(r_int, &r_bytes, &r_len) || !EcdsaIntField(s_int, &s_bytes, &s_len))
        return false;

    duetos::net::ec::Curve curve{};
    if (!duetos::net::ec::GetCurve(issuer_curve, &curve))
        return false;

    return duetos::net::ec::EcdsaVerify(curve, issuer_point, digest, digest_len, r_bytes, r_len, s_bytes, s_len);
}

// Verify that `issuer` is the signer of `cert`: dispatch on cert's outer
// signature algorithm. RSA certs must be signed by an RSA issuer key;
// ECDSA certs by an EC issuer key. A mismatch (e.g. an ECDSA-signed cert
// whose claimed issuer has no EC key) fails closed.
// Byte-exact DN comparison. RFC 5280 issuer/subject chaining is a
// canonical-form match in the general case, but the roots and certs we
// embed/serve use stable DER encodings, so an exact-bytes compare is the
// correct, cheap gate here. A mismatch means `issuer` did not issue
// `cert` and we must NOT spend an ECDSA/RSA verify on the pair.
bool DnEquals(const u8* a, u32 alen, const u8* b, u32 blen)
{
    if (a == nullptr || b == nullptr || alen == 0 || alen != blen)
        return false;
    for (u32 i = 0; i < alen; ++i)
    {
        if (a[i] != b[i])
            return false;
    }
    return true;
}

bool IssuerSigns(const ParsedCert& cert, const ParsedCert& issuer)
{
    // Name-chaining gate (RFC 5280 §6.1.3): the candidate issuer's
    // subject DN must equal the cert's issuer DN before we spend a
    // signature verify. This is both a correctness gate (a key that
    // belongs to a differently-named CA cannot anchor this cert) and
    // the boot-budget gate (skips the costly ECDSA P-384 verify against
    // every non-matching root in the trust store).
    if (!DnEquals(cert.issuer_dn, cert.issuer_dn_len, issuer.subject_dn, issuer.subject_dn_len))
        return false;

    switch (cert.sig_algo)
    {
    case CertSigAlgo::Sha256WithRsa:
        return issuer.base.subject_rsa_present && VerifySig(cert, issuer.base.subject_rsa);
    case CertSigAlgo::EcdsaWithSha256:
    case CertSigAlgo::EcdsaWithSha384:
        return issuer.ec_present && VerifySigEc(cert, issuer.ec_curve, issuer.ec_point);
    case CertSigAlgo::Other:
    default:
        return false; // GAP: RSA-PSS / Ed25519 / sha1WithRSA — fail closed.
    }
}

// Case-insensitive ASCII compare of a DNS label slice to a hostname
// span [hp, hp+hlen). Lengths must match.
bool DnsEqual(const u8* a, u32 alen, const char* hp, u32 hlen)
{
    if (alen != hlen)
        return false;
    for (u32 i = 0; i < alen; ++i)
    {
        u8 x = a[i];
        u8 y = static_cast<u8>(hp[i]);
        if (x >= 'A' && x <= 'Z')
            x = static_cast<u8>(x + 32);
        if (y >= 'A' && y <= 'Z')
            y = static_cast<u8>(y + 32);
        if (x != y)
            return false;
    }
    return true;
}

// Match one SAN dNSName pattern against the hostname. Supports a single
// leftmost "*." wildcard that matches exactly one label (RFC 6125):
// "*.example.com" matches "foo.example.com" but NOT "example.com" nor
// "a.b.example.com". A bare "*" or wildcard not in the leftmost label
// is rejected.
bool DnsNameMatches(const u8* pat, u32 patlen, const char* host, u32 hostlen)
{
    if (patlen == 0 || hostlen == 0)
        return false;

    // Wildcard form: must be exactly "*." prefix.
    if (pat[0] == '*')
    {
        if (patlen < 2 || pat[1] != '.')
            return false; // "*" alone or "*foo" — reject.
        // The wildcard covers the host's leftmost label. Find the first
        // dot in the host; the remainder (incl. the dot) must equal the
        // pattern remainder (incl. its dot).
        u32 hd = 0;
        while (hd < hostlen && host[hd] != '.')
            ++hd;
        if (hd == hostlen)
            return false; // host has no dot -> no label to consume.
        if (hd == 0)
            return false;                       // empty leftmost label.
        const char* host_rest = host + hd;      // points at '.'
        const u32 host_rest_len = hostlen - hd; // includes the dot
        const u8* pat_rest = pat + 1;           // points at '.'
        const u32 pat_rest_len = patlen - 1;    // includes the dot
        // Reject if the wildcard's tail itself contains another label
        // wildcard or the host remainder still has a wildcard-y char —
        // both spans are literal here, so just compare.
        return DnsEqual(pat_rest, pat_rest_len, host_rest, host_rest_len);
    }

    return DnsEqual(pat, patlen, host, hostlen);
}

bool HostMatches(const ParsedCert& leaf, const char* hostname)
{
    if (hostname == nullptr || hostname[0] == '\0')
        return false;
    u32 hlen = 0;
    while (hostname[hlen] != '\0')
        ++hlen;

    // Prefer SAN. RFC 6125 / 2818: if a SAN dNSName list is present, the
    // CN is NOT consulted. Only fall back to CN when no SAN dNSName.
    if (leaf.san_count > 0)
    {
        for (u32 i = 0; i < leaf.san_count; ++i)
            if (DnsNameMatches(leaf.san_dns[i], leaf.san_dns_len[i], hostname, hlen))
                return true;
        return false;
    }

    // CN fallback (with wildcard support).
    if (leaf.base.subject_cn != nullptr && leaf.base.subject_cn_len > 0)
        return DnsNameMatches(leaf.base.subject_cn, leaf.base.subject_cn_len, hostname, hlen);
    return false;
}

bool WithinValidity(const ParsedCert& c, u64 now_unix)
{
    if (c.not_before == ParsedCert::kBadTime || c.not_after == ParsedCert::kBadTime)
        return false;
    return now_unix >= c.not_before && now_unix <= c.not_after;
}

