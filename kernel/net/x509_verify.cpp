// The embedded trust store. Each anchor is a root certificate DER; we
// parse it once on first use and verify candidate signatures against
// its public key.
struct TrustAnchor
{
    const u8* der;
    u32 der_len;
};

constexpr TrustAnchor kTrustStore[] = {
    {kFixtureRootDer, static_cast<u32>(sizeof(kFixtureRootDer))},
};

bool TrustAnchorVerifies(const ParsedCert& cert, u64 now_unix)
{
    for (const TrustAnchor& anchor : kTrustStore)
    {
        ParsedCert root{};
        if (!ParseOne(anchor.der, anchor.der_len, &root))
            continue;
        if (!root.base.subject_rsa_present)
            continue;
        // The root must itself be valid at `now` — an expired anchor
        // anchors nothing.
        if (!WithinValidity(root, now_unix))
            continue;
        if (VerifySig(cert, root.base.subject_rsa))
            return true;
    }
    return false;
}

