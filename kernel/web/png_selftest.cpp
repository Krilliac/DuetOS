// Scratch arena big enough for the largest fixture. The 8x8 Adam7
// pair needs: inflated scanlines (8 rows * 9 bytes non-interlaced, or
// the summed Adam7 passes), RGBA out 8*8*4 = 256, plus IDAT staging
// sized at the input length (~144). 4 KiB is comfortable.
u8 g_arena_buf[4096];

bool Expect(bool cond, const char* tag, u32 code, u32& fail_code)
{
    if (!cond)
    {
        arch::SerialWrite("[png-selftest] FAIL ");
        arch::SerialWrite(tag);
        arch::SerialWrite("\n");
        if (fail_code == 0)
            fail_code = code;
    }
    return cond;
}

// Read an RGBA pixel from a decoded image.
bool PixelEq(const PngImage& img, u32 x, u32 y, u8 r, u8 g, u8 b, u8 a)
{
    if (x >= img.width || y >= img.height)
        return false;
    const u8* p = img.pixels + (u64(y) * img.width + x) * 4;
    return p[0] == r && p[1] == g && p[2] == b && p[3] == a;
}

