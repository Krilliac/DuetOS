/*
 * DuetOS — firmware-package trust root.
 *
 * Stores the public key(s) the kernel uses to authenticate signed
 * firmware-package envelopes. The matching private key signs offline
 * via `tools/build/fw-sign.py`; the kernel only ever does verify.
 *
 *   Threat model
 *
 *   The on-disk firmware package (DUETFWPK envelope) carries a
 *   SHA-256 of its payload. That alone is integrity — anyone with
 *   write access to the firmware file can edit the payload, recompute
 *   the digest, and the kernel happily loads the result. The trust-
 *   root signature is what makes the integrity field *authenticated*:
 *   only the holder of the signing private key can produce a trailer
 *   the kernel will accept.
 *
 *   Without this gate, a malware persistence path is:
 *     1. Write to /firmware/ as root, swapping the file for one with
 *        a NOP-sled-laden payload + recomputed digest.
 *     2. Wait for the next boot.
 *     3. The kernel uploads the payload to the device, the device
 *        executes it, and the device is now an attacker-controlled
 *        DMA-capable peripheral.
 *
 *   The fix is to require a trusted-root signature over the envelope.
 *   Below is the dev trust root committed in-tree; a production
 *   deployment replaces it via the kernel CMake `DUETOS_FW_TRUST_ROOT_*`
 *   options (a future slice — for now the dev key is the only path).
 *
 *   Identifier scheme
 *
 *   The trailer carries a 16-bit `pubkey_id` so a single kernel image
 *   can accept signatures from multiple keys (e.g. a transitional
 *   "old key" + "new key" during a rotation). `Lookup` walks the
 *   built-in array.
 *
 *     pubkey_id  source
 *     1          Dev key — `tools/build/fw-signing-keys/dev-fw-signing-private.pem`
 *                ONLY in-tree key; do not deploy this for production.
 *
 *   Production rotation is via CMake override: define
 *   `DUETOS_FW_TRUST_ROOT_HEADER` to point to a header that provides
 *   the same constants. Default falls through to the dev key.
 */

