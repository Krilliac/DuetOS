const u8* Font8x8Lookup(char ch)
{
    // Lowercase → uppercase: v0 alphabet has uppercase glyphs only.
    // Real lowercase shapes grow this table in a follow-up slice.
    if (ch >= 'a' && ch <= 'z')
    {
        ch = static_cast<char>(ch - 'a' + 'A');
    }
    switch (ch)
    {
    case ' ':
        return kGlyphSpace;
    case '0':
        return kGlyph0;
    case '1':
        return kGlyph1;
    case '2':
        return kGlyph2;
    case '3':
        return kGlyph3;
    case '4':
        return kGlyph4;
    case '5':
        return kGlyph5;
    case '6':
        return kGlyph6;
    case '7':
        return kGlyph7;
    case '8':
        return kGlyph8;
    case '9':
        return kGlyph9;
    case 'A':
        return kGlyphA;
    case 'B':
        return kGlyphB;
    case 'C':
        return kGlyphC;
    case 'D':
        return kGlyphD;
    case 'E':
        return kGlyphE;
    case 'F':
        return kGlyphF;
    case 'G':
        return kGlyphG;
    case 'H':
        return kGlyphH;
    case 'I':
        return kGlyphI;
    case 'J':
        return kGlyphJ;
    case 'K':
        return kGlyphK;
    case 'L':
        return kGlyphL;
    case 'M':
        return kGlyphM;
    case 'N':
        return kGlyphN;
    case 'O':
        return kGlyphO;
    case 'P':
        return kGlyphP;
    case 'Q':
        return kGlyphQ;
    case 'R':
        return kGlyphR;
    case 'S':
        return kGlyphS;
    case 'T':
        return kGlyphT;
    case 'U':
        return kGlyphU;
    case 'V':
        return kGlyphV;
    case 'W':
        return kGlyphW;
    case 'X':
        return kGlyphX;
    case 'Y':
        return kGlyphY;
    case 'Z':
        return kGlyphZ;
    case '.':
        return kGlyphPeriod;
    case ',':
        return kGlyphComma;
    case ':':
        return kGlyphColon;
    case ';':
        return kGlyphSemicolon;
    case '-':
        return kGlyphHyphen;
    case '_':
        return kGlyphUnderscore;
    case '/':
        return kGlyphSlash;
    case '\\':
        return kGlyphBackslash;
    case '!':
        return kGlyphExclaim;
    case '?':
        return kGlyphQuestion;
    case '(':
        return kGlyphLParen;
    case ')':
        return kGlyphRParen;
    case '[':
        return kGlyphLBracket;
    case ']':
        return kGlyphRBracket;
    case '<':
        return kGlyphLT;
    case '>':
        return kGlyphGT;
    case '=':
        return kGlyphEquals;
    case '+':
        return kGlyphPlus;
    case '*':
        return kGlyphStar;
    case '"':
        return kGlyphQuote;
    case '\'':
        return kGlyphApostrophe;
    default:
        return kGlyphBox;
    }
}

