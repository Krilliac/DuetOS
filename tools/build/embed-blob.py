#!/usr/bin/env python3
"""embed-blob.py — convert a binary file into a C++ constexpr
u8 array header for inclusion in the kernel image.

Usage:
    embed_blob.py <input_binary> <output_header> <symbol_name> [--namespace NS]
    embed_blob.py --empty       <output_header> <symbol_name> [--namespace NS]

The generated header declares:
    namespace <NS> {
    inline constexpr unsigned char <symbol_name>[] = { 0xMM, ... };
    inline constexpr unsigned long long <symbol_name>_len = sizeof(<symbol_name>);
    } // namespace

Matches the hand-laid-out byte arrays already used in
kernel/fs/ramfs.cpp (see kBinExitElfBytes) so the generated
file drops straight into the same ramfs node shape.

`--empty` emits a 1-byte sentinel array with `<symbol>_len = 0`,
for the case where the source binary cannot be produced (e.g.
the mingw-w64 cross-compiler isn't installed). Kernel consumers
that check `_len == 0` short-circuit gracefully; the single
sentinel byte keeps the array a well-formed C++ object so the
header still links.

No external dependencies — stdlib only.
"""

import argparse
import os
import sys
from pathlib import Path


def write_header(output: Path, symbol: str, namespace: str, data: bytes, *, len_override: int | None = None,
                 source_label: str) -> None:
    """Write the C++ header. If `len_override` is set, the `<symbol>_len`
    constant is forced to that value rather than `sizeof(<symbol>)` —
    used by `--empty` to publish `len = 0` even though the array carries
    a 1-byte sentinel."""
    ns_parts = namespace.split("::") if namespace else []
    lines = []
    lines.append("// AUTO-GENERATED by tools/build/embed_blob.py. DO NOT EDIT.")
    lines.append(f"// Source: {source_label}  ({len(data)} bytes in array)")
    lines.append("#pragma once")
    lines.append("")
    for ns in ns_parts:
        lines.append(f"namespace {ns} {{")
    lines.append("")
    lines.append(f"inline constexpr unsigned char {symbol}[] = {{")

    for row_start in range(0, len(data), 16):
        row = data[row_start:row_start + 16]
        hex_row = ", ".join(f"0x{b:02X}" for b in row)
        lines.append(f"    {hex_row},")
    lines.append("};")
    lines.append("")
    if len_override is None:
        lines.append(f"inline constexpr unsigned long long {symbol}_len = sizeof({symbol});")
    else:
        lines.append(f"inline constexpr unsigned long long {symbol}_len = {len_override};")
    lines.append("")
    for ns in reversed(ns_parts):
        lines.append(f"}} // namespace {ns}")
    lines.append("")

    output.parent.mkdir(parents=True, exist_ok=True)
    output.write_text("\n".join(lines))


def main() -> int:
    p = argparse.ArgumentParser(description=__doc__, formatter_class=argparse.RawDescriptionHelpFormatter)
    p.add_argument("input", nargs="?", type=Path,
                   help="Binary file to embed (omit when --empty is set)")
    p.add_argument("output", type=Path, help="Output .h file")
    p.add_argument("symbol", help="C++ symbol name for the array (e.g. kBinHelloPeBytes)")
    p.add_argument("--namespace", default="duetos::fs::generated",
                   help="Namespace to wrap the symbol in (default: duetos::fs::generated)")
    p.add_argument("--empty", action="store_true",
                   help="Emit a stub array with _len=0 instead of reading <input>. "
                        "Used when the source binary cannot be produced (missing toolchain).")
    args = p.parse_args()

    if args.empty:
        # One-byte sentinel array, `_len = 0`. Kernel consumers that
        # gate on `_len == 0` skip the blob; the byte keeps the array
        # a well-formed object so the header still links.
        write_header(args.output, args.symbol, args.namespace, b"\x00",
                     len_override=0, source_label="<empty stub>")
        return 0

    if args.input is None:
        print("embed_blob.py: <input> required unless --empty is set", file=sys.stderr)
        return 2
    if not args.input.is_file():
        print(f"embed_blob.py: input not found: {args.input}", file=sys.stderr)
        return 2

    write_header(args.output, args.symbol, args.namespace, args.input.read_bytes(),
                 source_label=args.input.name)
    return 0


if __name__ == "__main__":
    sys.exit(main())
