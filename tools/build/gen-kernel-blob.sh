#!/usr/bin/env bash
# tools/build/gen-kernel-blob.sh
#
# Emit a tiny .S file that .incbins a binary blob (typically the
# stage-1 kernel ELF) and exposes three extern "C" symbols the
# kernel can read at runtime: kBinKernelElfBytes (start),
# kBinKernelElfBytes_end (one-past-end), kBinKernelElfBytes_len
# (a u64 holding the size).
#
# Usage:
#   gen-kernel-blob.sh <out.S> <blob-path>      — embed real bytes
#   gen-kernel-blob.sh <out.S> --empty          — emit zero-len stub
#
# `.incbin` is processed by the assembler in constant time, no
# matter how big the input. A constexpr-literal alternative would
# balloon compile time linearly with file size.

set -euo pipefail

if [[ $# -ne 2 ]]; then
    echo "usage: $0 <out.S> <blob-path|--empty>" >&2
    exit 2
fi

OUT="$1"
BLOB="$2"

if [[ "${BLOB}" == "--empty" ]]; then
    cat > "${OUT}" <<'EOF'
/* Auto-generated by tools/build/gen-kernel-blob.sh — do not edit. */
/* DUETOS_INSTALLER_KERNEL_EMBED=OFF: zero-byte stub. */
/* Lives in .kernel_elf_blob (pinned at phys 32 MiB by the linker
 * script) so the embedded ELF never consumes the 0..16 MiB DMA zone.
 * Zero-length here, so the section is empty and costs nothing. */
.section .kernel_elf_blob, "a"
.global kBinKernelElfBytes
.global kBinKernelElfBytes_end
.global kBinKernelElfBytes_len
.balign 16
kBinKernelElfBytes:
kBinKernelElfBytes_end:
.balign 8
kBinKernelElfBytes_len:
.quad 0
EOF
    exit 0
fi

if [[ ! -f "${BLOB}" ]]; then
    echo "gen-kernel-blob.sh: blob path not found: ${BLOB}" >&2
    exit 2
fi

cat > "${OUT}" <<EOF
/* Auto-generated by tools/build/gen-kernel-blob.sh — do not edit. */
/* DUETOS_INSTALLER_KERNEL_EMBED=ON: embeds stage-1 kernel ELF. */
/* Lives in .kernel_elf_blob (pinned at phys 32 MiB by the linker
 * script) so the embedded ~10 MiB ELF loads ABOVE the 0..16 MiB
 * legacy DMA zone instead of swallowing it. */
.section .kernel_elf_blob, "a"
.global kBinKernelElfBytes
.global kBinKernelElfBytes_end
.global kBinKernelElfBytes_len
.balign 16
kBinKernelElfBytes:
.incbin "${BLOB}"
kBinKernelElfBytes_end:
.balign 8
kBinKernelElfBytes_len:
.quad kBinKernelElfBytes_end - kBinKernelElfBytes
EOF
