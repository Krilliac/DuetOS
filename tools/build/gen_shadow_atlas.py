#!/usr/bin/env python3
"""Bake the 32x32 quarter-circle shadow corner atlas into a constexpr u32 array.

Falloff: alpha(d) = 255 * (1 - d/32) ** 2 where d = Euclidean distance from
the corner origin (0, 0). RGB is always 0x000000 (black tint); the colour
tint is applied at render time by RenderSoftShadow.

Output: generated_shadow_atlas.h.
"""
import sys, math, pathlib

SIZE = 32

def alpha_at(x: int, y: int) -> int:
    d = math.hypot(x, y)
    if d >= SIZE:
        return 0
    f = (1.0 - d / SIZE) ** 2
    return max(0, min(255, int(round(255 * f))))

def main():
    out_path = pathlib.Path(sys.argv[1])
    pixels = []
    for y in range(SIZE):
        for x in range(SIZE):
            a = alpha_at(x, y)
            pixels.append(f"0x{a:02X}000000")
    lines = ["#pragma once",
             "// Auto-generated by tools/build/gen_shadow_atlas.py. Do not edit.",
             "#include \"util/types.h\"",
             "namespace duetos::drivers::video {",
             f"inline constexpr u32 kShadowAtlasSize = {SIZE};",
             "inline constexpr u32 kShadowAtlas[] = {"]
    for i in range(0, len(pixels), 8):
        lines.append("    " + ", ".join(pixels[i:i+8]) + ",")
    lines.append("};")
    lines.append("}")
    out_path.write_text("\n".join(lines) + "\n")

if __name__ == "__main__":
    main()
