#!/usr/bin/env python3
"""Regenerate AUTO sections in wiki/ from source-of-truth artifacts.

The wiki convention is described in `wiki/tooling/Wiki-Auto-Sections.md`.
In short:

    <!-- AUTO:<section-id> [key=value ...] START -->
    ...generated content...
    <!-- AUTO:<section-id> END -->

A generator is a Python function returning the body text for one
AUTO block. The set of registered generators is `_GENERATORS` at
the bottom of this file.

Usage:

    # Regenerate every AUTO block in the wiki/ tree:
    python3 tools/build/gen-wiki-auto.py

    # Check-only (CI gate): exit 1 if any block on disk differs from
    # what would be regenerated.
    python3 tools/build/gen-wiki-auto.py --check

    # Limit to specific files:
    python3 tools/build/gen-wiki-auto.py wiki/reference/Win32-Surface-Status.md

The generator contract:

    def gen_foo(args: dict[str, str], repo_root: Path) -> str:
        '''Return the body of the AUTO:foo block.

        - args: the key=value pairs from the START marker.
        - Body must NOT include the START/END markers.
        - First line should be a `<!-- generated by ... -->`
          banner so a casual reader spots that it's auto.
        - Trailing newline is added by the framework — emit
          the body without one.
        '''

The framework writes the body verbatim between the markers and
preserves all hand-written content outside them.
"""

from __future__ import annotations

import argparse
import re
import sys
from collections import defaultdict
from pathlib import Path

# Marker shape: <!-- AUTO:<id> [k=v ...] START --> ... <!-- AUTO:<id> END -->
# Captured groups: (1) section_id, (2) raw arg blob (may be empty),
# (3) inner body (anything between START and END), (4) section_id again
# (must match the START id; the framework asserts this).
_MARKER_RE = re.compile(
    r"<!-- AUTO:([A-Za-z0-9_\-]+)((?:\s+[A-Za-z0-9_]+=[^\s>]+)*)\s+START -->"
    r"(.*?)"
    r"<!-- AUTO:\1 END -->",
    re.DOTALL,
)

_BANNER = "<!-- generated by tools/build/gen-wiki-auto.py — do not edit by hand -->"


def _parse_args(arg_blob: str) -> dict[str, str]:
    """Parse `key=value key=value` pairs from a marker's arg blob."""
    out: dict[str, str] = {}
    for token in arg_blob.strip().split():
        if "=" not in token:
            continue
        k, v = token.split("=", 1)
        out[k] = v
    return out


# ---------------------------------------------------------------- generators


# Friendly per-offset disposition. The thunks_table.inc rows route
# Win32 imports to a small, fixed set of compile-time offsets in
# `kernel/subsystems/win32/thunks.cpp`. Each offset is either:
#   - a real implementation (e.g. `kOffExitProcess` calls into the
#     kernel's process-exit path),
#   - a generic noop (e.g. `kOffReturnZero`, `kOffCritSecNop`),
#   - an explicit STUB tagged in the offset name (`*Stub`) or the
#     last-resort catch-all (`kOffMissLogger`).
# The classification below is the heuristic the wiki uses: anything
# not on the explicit STUB list is treated as REAL even when the
# implementation is "return a constant" — that constant IS the
# documented behaviour for those calls.
_NOOP_OFFSETS = {
    "kOffReturnZero",
    "kOffReturnOne",
    "kOffCritSecNop",
}
# NOTE: kOffGetProcessHeap is NOT a NOOP — its thunk returns the
# process-heap handle (the kProcEnvVa pointer), which is the same
# stable cookie every subsequent HeapAlloc/HeapFree ignores. The
# Win32 contract for GetProcessHeap is "return a fixed non-NULL
# handle"; returning kProcEnvVa satisfies it. Previously flagged
# as NOOP because the bytecode is a single `mov rax, imm; ret`,
# but that's the right shape for "return a fixed handle".
_EXPLICIT_STUB_OFFSETS = {"kOffMissLogger"}

# Cached parse of thunks_table.inc shared across generator calls.
_THUNKS_CACHE: list[tuple[str, str, str]] | None = None


def _load_thunks(repo_root: Path) -> list[tuple[str, str, str]]:
    """Parse thunks_table.inc into (dll, fn, kOff*) tuples.

    Same shape as `gen-fix-patches.py:load_thunks_table`. Cached so
    a single CLI run pays the parse once even when many generators
    fire."""
    global _THUNKS_CACHE
    if _THUNKS_CACHE is not None:
        return _THUNKS_CACHE
    path = repo_root / "kernel" / "subsystems" / "win32" / "thunks_table.inc"
    text = path.read_text(encoding="utf-8")
    out: list[tuple[str, str, str]] = []
    for m in re.finditer(
        r'\{\s*"([^"]+)"\s*,\s*"([^"]+)"\s*,\s*(kOff\w+)\s*\}', text
    ):
        out.append((m.group(1), m.group(2), m.group(3)))
    _THUNKS_CACHE = out
    return out


def _classify_offset(off: str) -> str:
    """REAL / STUB / NOOP — see _NOOP_OFFSETS / _EXPLICIT_STUB_OFFSETS."""
    if off in _EXPLICIT_STUB_OFFSETS or off.endswith("Stub"):
        return "STUB"
    if off in _NOOP_OFFSETS:
        return "NOOP"
    return "REAL"


def gen_thunks_by_dll(args: dict[str, str], repo_root: Path) -> str:
    """Render a markdown table of all thunks_table.inc rows for one DLL.

    Required arg: `DLL=<filename>` (case-insensitive). The output
    table has one row per import: method, status (REAL / STUB /
    NOOP), routed offset. Methods are sorted alphabetically so the
    output stays stable across re-orderings of thunks_table.inc.
    """
    dll = args.get("DLL")
    if dll is None:
        return f"{_BANNER}\n\n_(error: thunks-by-dll requires `DLL=<name>`)_"
    rows = [
        (fn, _classify_offset(off), off)
        for (rdll, fn, off) in _load_thunks(repo_root)
        if rdll.lower() == dll.lower()
    ]
    if not rows:
        return (
            f"{_BANNER}\n\n"
            f"_(no thunks_table.inc rows for `{dll}`)_"
        )
    rows.sort(key=lambda r: r[0].lower())
    counts = defaultdict(int)
    for _, status, _o in rows:
        counts[status] += 1
    out: list[str] = [_BANNER, ""]
    summary_parts = [f"{counts[k]} {k}" for k in ("REAL", "STUB", "NOOP") if counts[k]]
    out.append(
        f"**`{dll}`** — {len(rows)} imports thunked: " + ", ".join(summary_parts) + "."
    )
    out.append("")
    out.append("| Method | Status | Routed to |")
    out.append("|--------|--------|-----------|")
    for fn, status, off in rows:
        out.append(f"| `{fn}` | {status} | `{off}` |")
    return "\n".join(out)


# Cached parse of FixDetector enum values.
_DETECTORS_CACHE: list[tuple[int, str, str]] | None = None


def _load_fix_detectors(repo_root: Path) -> list[tuple[int, str, str]]:
    """Extract `FixDetector` enum entries.

    Returns (value, name, comment) tuples in declaration order. The
    enum lives in `kernel/diag/fix_journal.h`; we parse the explicit
    `= N` form for stability."""
    global _DETECTORS_CACHE
    if _DETECTORS_CACHE is not None:
        return _DETECTORS_CACHE
    path = repo_root / "kernel" / "diag" / "fix_journal.h"
    text = path.read_text(encoding="utf-8")
    m = re.search(
        r"enum class FixDetector\s*:\s*u8\s*\{(.+?)\};",
        text,
        re.DOTALL,
    )
    if m is None:
        _DETECTORS_CACHE = []
        return []
    body = m.group(1)
    out: list[tuple[int, str, str]] = []
    for line in body.splitlines():
        em = re.match(
            r"\s*([A-Za-z][A-Za-z0-9_]*)\s*=\s*(\d+)\s*,?\s*(?://\s*(.*))?",
            line,
        )
        if em is None:
            continue
        name = em.group(1)
        value = int(em.group(2))
        comment = (em.group(3) or "").strip()
        out.append((value, name, comment))
    _DETECTORS_CACHE = out
    return out


def gen_journal_detectors(args: dict[str, str], repo_root: Path) -> str:
    """Render the FixDetector enum as a markdown table.

    No args. Each row carries the numeric value, the enum name, and
    the inline `// comment` from the header (truncated past the
    first line if multi-line).
    """
    rows = _load_fix_detectors(repo_root)
    out: list[str] = [_BANNER, ""]
    if not rows:
        out.append("_(failed to parse `FixDetector` from `kernel/diag/fix_journal.h`)_")
        return "\n".join(out)
    out.append("Source: `kernel/diag/fix_journal.h` — the on-disk record format. "
               "These values are stable IDs (appended-to, never renumbered).")
    out.append("")
    out.append("| Value | Name | Description |")
    out.append("|-------|------|-------------|")
    for value, name, comment in rows:
        out.append(f"| {value} | `{name}` | {comment} |")
    return "\n".join(out)


def _load_shell_commands(repo_root: Path) -> list[tuple[str, str]]:
    """Walk kernel/shell/*.cpp for `void Cmd<Name>(...)` declarations.

    Returns (function-name, source-path) tuples. The shell dispatcher
    binds these to operator-typeable commands; the surface is wide
    enough that the wiki page maintained by hand routinely drifts."""
    sh_dir = repo_root / "kernel" / "shell"
    cmd_re = re.compile(r"^\s*void\s+(Cmd[A-Z][A-Za-z0-9_]*)\s*\(", re.MULTILINE)
    out: list[tuple[str, str]] = []
    for cpp in sorted(sh_dir.glob("*.cpp")):
        text = cpp.read_text(encoding="utf-8")
        for m in cmd_re.finditer(text):
            out.append((m.group(1), str(cpp.relative_to(repo_root))))
    return out


def gen_shell_commands(args: dict[str, str], repo_root: Path) -> str:
    """Render every `Cmd*` shell handler under kernel/shell/.

    No args. Output is grouped by source file (one section per
    shell_*.cpp) so a reader can spot the file owning a given
    command at a glance."""
    rows = _load_shell_commands(repo_root)
    if not rows:
        return f"{_BANNER}\n\n_(no `Cmd*` symbols found under `kernel/shell/`)_"
    by_file: dict[str, list[str]] = defaultdict(list)
    for name, file_rel in rows:
        by_file[file_rel].append(name)
    out: list[str] = [_BANNER, ""]
    out.append(
        f"Source: `kernel/shell/*.cpp` — every `void Cmd*(...)` symbol "
        f"appears here. Total: **{len(rows)}** handlers across "
        f"**{len(by_file)}** TUs."
    )
    out.append("")
    out.append("| Source | Handlers |")
    out.append("|--------|----------|")
    for file_rel in sorted(by_file):
        names = sorted(by_file[file_rel])
        names_md = ", ".join(f"`{n}`" for n in names)
        out.append(f"| `{file_rel}` | {names_md} |")
    return "\n".join(out)


_GENERATORS = {
    "thunks-by-dll": gen_thunks_by_dll,
    "journal-detectors": gen_journal_detectors,
    "shell-commands": gen_shell_commands,
}


# ---------------------------------------------------------------- driver


def regenerate_text(text: str, repo_root: Path) -> tuple[str, int]:
    """Walk a single file's content and regenerate every AUTO block.

    Returns (new_text, blocks_processed). Unknown section IDs are
    left untouched so the framework can be partially rolled out
    without breaking pages that reference future generators.
    """
    blocks_processed = 0

    def repl(m: re.Match[str]) -> str:
        nonlocal blocks_processed
        section_id = m.group(1)
        arg_blob = m.group(2)
        gen = _GENERATORS.get(section_id)
        if gen is None:
            return m.group(0)  # leave untouched
        args = _parse_args(arg_blob)
        body = gen(args, repo_root)
        body = body.rstrip("\n")
        blocks_processed += 1
        # Reconstruct with the original START / END markers verbatim
        # so any whitespace nuances in the marker line (alignment,
        # extra args we didn't touch) are preserved.
        start_marker = f"<!-- AUTO:{section_id}{arg_blob} START -->"
        end_marker = f"<!-- AUTO:{section_id} END -->"
        return f"{start_marker}\n{body}\n{end_marker}"

    new_text = _MARKER_RE.sub(repl, text)
    return new_text, blocks_processed


def main() -> int:
    ap = argparse.ArgumentParser(description=__doc__)
    ap.add_argument(
        "files",
        nargs="*",
        type=Path,
        help="files to regenerate (default: every wiki/**/*.md)",
    )
    ap.add_argument(
        "--check",
        action="store_true",
        help="exit 1 if any AUTO block on disk differs from regenerated; do not write",
    )
    ap.add_argument(
        "--repo-root",
        type=Path,
        default=None,
        help="repo root (auto-detected from this script's location by default)",
    )
    args = ap.parse_args()

    repo_root = args.repo_root
    if repo_root is None:
        # tools/build/gen-wiki-auto.py -> repo root is two levels up.
        repo_root = Path(__file__).resolve().parent.parent.parent
    repo_root = repo_root.resolve()

    targets: list[Path]
    if args.files:
        targets = [p.resolve() for p in args.files]
    else:
        targets = sorted((repo_root / "wiki").rglob("*.md"))

    drift = 0
    rewritten = 0
    blocks_total = 0
    for path in targets:
        if not path.exists():
            print(f"# skip (missing): {path}", file=sys.stderr)
            continue
        original = path.read_text(encoding="utf-8")
        if "<!-- AUTO:" not in original:
            continue
        new_text, blocks = regenerate_text(original, repo_root)
        blocks_total += blocks
        if new_text == original:
            continue
        rel = path.relative_to(repo_root) if path.is_absolute() else path
        if args.check:
            print(f"# DRIFT: {rel} ({blocks} block(s) would change)", file=sys.stderr)
            drift += 1
            continue
        path.write_text(new_text, encoding="utf-8")
        print(f"# wrote: {rel} ({blocks} block(s) regenerated)", file=sys.stderr)
        rewritten += 1

    if args.check:
        if drift:
            print(
                f"# {drift} file(s) drifted; run `tools/build/gen-wiki-auto.py` to regenerate",
                file=sys.stderr,
            )
            return 1
        print(f"# OK: no drift in {len(targets)} file(s)", file=sys.stderr)
        return 0

    print(
        f"# done: {rewritten} file(s) rewritten, {blocks_total} AUTO block(s) processed total",
        file=sys.stderr,
    )
    return 0


if __name__ == "__main__":
    sys.exit(main())
