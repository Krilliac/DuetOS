#!/usr/bin/env python3
"""embed-blob.py — convert a binary file into a C++ constexpr
u8 array header for inclusion in the kernel image.

Usage:
    embed_blob.py <input_binary> <output_header> <symbol_name> [--namespace NS]

The generated header declares:
    namespace <NS> {
    inline constexpr unsigned char <symbol_name>[] = { 0xMM, ... };
    inline constexpr unsigned long long <symbol_name>_len = sizeof(<symbol_name>);
    } // namespace

Matches the hand-laid-out byte arrays already used in
kernel/fs/ramfs.cpp (see kBinExitElfBytes) so the generated
file drops straight into the same ramfs node shape.

No external dependencies — stdlib only.
"""

import argparse
import os
import sys
from pathlib import Path


def main() -> int:
    p = argparse.ArgumentParser(description=__doc__, formatter_class=argparse.RawDescriptionHelpFormatter)
    p.add_argument("input", type=Path, help="Binary file to embed")
    p.add_argument("output", type=Path, help="Output .h file")
    p.add_argument("symbol", help="C++ symbol name for the array (e.g. kBinHelloPeBytes)")
    p.add_argument("--namespace", default="duetos::fs::generated",
                   help="Namespace to wrap the symbol in (default: duetos::fs::generated)")
    args = p.parse_args()

    if not args.input.is_file():
        print(f"embed_blob.py: input not found: {args.input}", file=sys.stderr)
        return 2

    data = args.input.read_bytes()
    ns_parts = args.namespace.split("::") if args.namespace else []

    lines = []
    lines.append("// AUTO-GENERATED by tools/build/embed_blob.py. DO NOT EDIT.")
    lines.append(f"// Source: {args.input.name}  ({len(data)} bytes)")
    lines.append("#pragma once")
    lines.append("")
    for ns in ns_parts:
        lines.append(f"namespace {ns} {{")
    lines.append("")
    lines.append(f"inline constexpr unsigned char {args.symbol}[] = {{")

    # 16 bytes per row, two-space indent. Matches the existing
    # hand-laid ramfs byte arrays.
    for row_start in range(0, len(data), 16):
        row = data[row_start:row_start + 16]
        hex_row = ", ".join(f"0x{b:02X}" for b in row)
        lines.append(f"    {hex_row},")
    lines.append("};")
    lines.append("")
    lines.append(f"inline constexpr unsigned long long {args.symbol}_len = sizeof({args.symbol});")
    lines.append("")
    for ns in reversed(ns_parts):
        lines.append(f"}} // namespace {ns}")
    lines.append("")

    args.output.parent.mkdir(parents=True, exist_ok=True)
    args.output.write_text("\n".join(lines))
    return 0


if __name__ == "__main__":
    sys.exit(main())
